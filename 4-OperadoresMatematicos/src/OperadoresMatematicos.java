
public class OperadoresMatematicos {
	public static void main(String[] args) {
		int x = 3;
		int y = 56;
		
		int res = x + y;
		System.out.println("Soma = " + res);
		
		//operadores
		/*
		 * subtracao - 
		 * divisao /
		 * multiplicao *
		 * Modulo %
		 */
		
		float var_float = (float) x / (float) y;
		System.out.println("Divisao = " + var_float);
		
		System.out.println("Modulo = " + (y % 10));
	}
}
