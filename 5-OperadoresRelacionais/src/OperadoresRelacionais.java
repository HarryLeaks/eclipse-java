
public class OperadoresRelacionais {
	public static void main(String[] args) {
		//operadores relacionais
		//boolean
		boolean bool = false;
		int x, y;
		
		x = 7;
		y = 5;
		bool = x < y;
		
		System.out.println(bool); //imprime false
		System.out.println(x > y); //imprime true 
		
		//operadores logicos
		//&& - || - !
		//e  - ou - not
	}
}
